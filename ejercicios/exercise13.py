# 13. Crear un programa que pida al usuario ingresar dos números y calcule su suma, resta, multiplicación y
# división.

num_1 = float(input("Type a number: "))

num_2 = float(input("Type a second number: "))

print(f"The sum of {num_1} and {num_2} is {num_1+num_2}")

print(f"The subtraction of {num_1} and {num_2} is {num_1-num_2}")

print(f"{num_1} times {num_2} is {num_1*num_2}")

print(f"{num_1} / {num_2} is {num_1/num_2}")